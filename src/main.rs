fn main() {
    println!("Hello, world!");
}
// This is the entry point for the Rust program. It prints "Hello, world!" to the console.
// The `main` function is the starting point of the program execution.
// The `println!` macro is used to print text to the console.
// The `!` indicates that `println` is a macro, not a function.
// The text inside the quotes is the message that will be displayed.